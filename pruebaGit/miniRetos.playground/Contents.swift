//: Playground - noun: a place where people can play

import UIKit

var num = 0...100


for i in num {
    
    
    if i  % 5 == 0
    {
        print("\(i) BINGO")
    }
    
    if i  % 2 == 0
    {
        print("\(i) PAR")
    }
    
    if i % 2 != 0
    {
        print("\(i) IMPAR")
    }
    if i >= 30 && i <= 40
    {
        print("\(i) VIVA SWIFT")
    }
    
    
}

